#include <stdio.h>
#include <unistd.h>

int	ft_recursive_power(int nb, int power)
{
	int	exr;

	exr = nb;
	if (power > 1)
		return (nb * ft_recursive_power(nb, (power - 1)));
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	return (exr);
}
